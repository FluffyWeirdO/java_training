    private String group;

    @Column(name = "email")
    @Type(type = "text")
    private String email1;

    @Column(name = "email2")
    @Type(type = "text")
    private String email2;

    @Column(name = "email3")
    @Type(type = "text")
    private String email3;

    @Transient
    private String allEmails;

    @Transient
    private String allDetails;

    //    @Column(name = "photo")
//    @Type(type = "text")
    @Transient
    private File photo;

